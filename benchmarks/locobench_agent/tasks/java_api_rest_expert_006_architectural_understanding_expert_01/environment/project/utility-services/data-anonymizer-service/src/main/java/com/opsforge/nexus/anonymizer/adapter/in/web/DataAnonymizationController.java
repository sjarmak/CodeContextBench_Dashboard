package com.opsforge.nexus.anonymizer.adapter.in.web;

import com.opsforge.nexus.anonymizer.application.port.in.DataAnonymizationUseCase;
import com.opsforge.nexus.anonymizer.application.port.in.command.AnonymizeDataCommand;
import com.opsforge.nexus.anonymizer.application.port.in.command.AnonymizeFileCommand;
import com.opsforge.nexus.anonymizer.domain.model.AnonymizedPayload;
import com.opsforge.nexus.commons.web.ErrorResponse;
import io.swagger.v3.oas.annotations.Operation;
import io.swagger.v3.oas.annotations.Parameter;
import io.swagger.v3.oas.annotations.media.Content;
import io.swagger.v3.oas.annotations.media.Schema;
import io.swagger.v3.oas.annotations.responses.ApiResponse;
import jakarta.validation.Valid;
import jakarta.validation.constraints.NotEmpty;
import jakarta.validation.constraints.NotNull;
import org.apache.commons.io.FilenameUtils;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.core.io.ByteArrayResource;
import org.springframework.http.CacheControl;
import org.springframework.http.HttpHeaders;
import org.springframework.http.HttpStatus;
import org.springframework.http.MediaType;
import org.springframework.http.ResponseEntity;
import org.springframework.util.unit.DataSize;
import org.springframework.validation.annotation.Validated;
import org.springframework.web.bind.annotation.*;
import org.springframework.web.multipart.MultipartFile;

import java.time.Duration;
import java.util.Collections;
import java.util.Map;
import java.util.UUID;

/**
 * REST Adapter that exposes JSON & CSV anonymization endpoints.
 * <p>
 *     This controller is intentionally thinâ€”its sole responsibility is to translate HTTP details
 *     into application-layer commands and to turn use-case results back into an HTTP-friendly form.
 *     All business logic lives in {@link DataAnonymizationUseCase}.
 * </p>
 */
@RestController
@RequestMapping("/api/v1/anonymizer")
@Validated
public class DataAnonymizationController {

    private static final Logger log = LoggerFactory.getLogger(DataAnonymizationController.class);
    private static final String CORRELATION_ID_HEADER = "X-Correlation-Id";
    private static final DataSize MAX_UPLOAD_SIZE = DataSize.ofMegabytes(25);

    private final DataAnonymizationUseCase anonymizationUseCase;

    public DataAnonymizationController(final DataAnonymizationUseCase anonymizationUseCase) {
        this.anonymizationUseCase = anonymizationUseCase;
    }

    // ---------------------------------------------------------------------------------------------
    // JSON payload anonymization
    // ---------------------------------------------------------------------------------------------

    @Operation(
            summary = "Anonymizes an arbitrary JSON payload",
            description = """
                    Applies the configured anonymization strategies to the provided JSON payload and \
                    returns a best-effort structurally-equivalent, but personally non-identifiable, result.
                    """,
            responses = {
                    @ApiResponse(
                            responseCode = "200",
                            description = "Payload anonymized",
                            content = @Content(mediaType = "application/json",
                                               schema = @Schema(implementation = JsonAnonymizationResponse.class))
                    ),
                    @ApiResponse(
                            responseCode = "400",
                            description = "Invalid request",
                            content = @Content(schema = @Schema(implementation = ErrorResponse.class))
                    )
            }
    )
    @PostMapping(
            path = "/json",
            consumes = MediaType.APPLICATION_JSON_VALUE,
            produces = MediaType.APPLICATION_JSON_VALUE
    )
    public ResponseEntity<JsonAnonymizationResponse> anonymizeJson(
            @Valid @RequestBody JsonAnonymizationRequest request,
            @RequestHeader(value = CORRELATION_ID_HEADER, required = false)
            @Parameter(description = "Optional correlation identifier generated by the caller")
            String correlationId
    ) {
        final String cid = resolveCorrelationId(correlationId);
        log.debug("[cid:{}] Received anonymization request for JSON payload", cid);

        AnonymizeDataCommand command = AnonymizeDataCommand.builder()
                .correlationId(cid)
                .payload(request.payload())
                .rules(request.rules())
                .build();

        AnonymizedPayload anonymized = anonymizationUseCase.anonymize(command);
        JsonAnonymizationResponse response = new JsonAnonymizationResponse(anonymized.payload());

        return ResponseEntity.ok()
                .header(CORRELATION_ID_HEADER, cid)
                // JSON responses are tiny; safe to cache for a short period
                .cacheControl(CacheControl.maxAge(Duration.ofSeconds(30)))
                .body(response);
    }

    // ---------------------------------------------------------------------------------------------
    // CSV / file anonymization
    // ---------------------------------------------------------------------------------------------

    @Operation(
            summary = "Anonymizes a CSV or TSV document",
            description = """
                    Streams the uploaded file through the anonymization engine and returns the \
                    anonymized document as a binary download. The system determines the file type \
                    from its original name; only CSV or TSV are supported.
                    """,
            responses = {
                    @ApiResponse(
                            responseCode = "200",
                            description = "File anonymized",
                            content = @Content(mediaType = "text/csv")
                    ),
                    @ApiResponse(
                            responseCode = "400",
                            description = "Unsupported format or invalid request",
                            content = @Content(schema = @Schema(implementation = ErrorResponse.class))
                    )
            }
    )
    @PostMapping(
            path = "/file",
            consumes = MediaType.MULTIPART_FORM_DATA_VALUE,
            produces = MediaType.APPLICATION_OCTET_STREAM_VALUE
    )
    public ResponseEntity<ByteArrayResource> anonymizeFile(
            @RequestPart("file") MultipartFile file,
            @RequestParam(name = "rule-set", required = false)
            @Parameter(description = "Pre-configured rule-set identifier") String ruleSet,
            @RequestHeader(value = CORRELATION_ID_HEADER, required = false) String correlationId
    ) {
        final String cid = resolveCorrelationId(correlationId);
        log.debug("[cid:{}] Received anonymization request for file '{}'", cid, file.getOriginalFilename());

        validateUpload(file);

        String extension = FilenameUtils.getExtension(file.getOriginalFilename()).toLowerCase();
        if (!"csv".equals(extension) && !"tsv".equals(extension)) {
            throw new IllegalArgumentException("Only CSV or TSV files are supported");
        }

        AnonymizeFileCommand command = AnonymizeFileCommand.builder()
                .correlationId(cid)
                .filename(file.getOriginalFilename())
                .content(file)
                .ruleSet(ruleSet)
                .build();

        byte[] content = anonymizationUseCase.anonymize(command);
        ByteArrayResource resource = new ByteArrayResource(content);

        HttpHeaders headers = new HttpHeaders();
        headers.setContentLength(content.length);
        headers.setContentDispositionFormData(
                "attachment",
                FilenameUtils.getBaseName(file.getOriginalFilename()) + "_anonymized." + extension
        );
        headers.set(CORRELATION_ID_HEADER, cid);

        return ResponseEntity.ok()
                .headers(headers)
                .cacheControl(CacheControl.noStore()) // never cache binary downloads
                .contentType("tsv".equals(extension) ? MediaType.TEXT_PLAIN : MediaType.TEXT_CSV)
                .body(resource);
    }

    // ---------------------------------------------------------------------------------------------
    // Exception Handling
    // ---------------------------------------------------------------------------------------------

    @ExceptionHandler(IllegalArgumentException.class)
    public ResponseEntity<ErrorResponse> handleBadRequest(IllegalArgumentException ex) {
        ErrorResponse error = new ErrorResponse(HttpStatus.BAD_REQUEST.value(),
                                                "Bad Request: " + ex.getMessage(),
                                                Collections.emptyList());

        return ResponseEntity.badRequest()
                .cacheControl(CacheControl.noStore())
                .body(error);
    }

    // ---------------------------------------------------------------------------------------------
    // Internal helpers
    // ---------------------------------------------------------------------------------------------

    private void validateUpload(MultipartFile file) {
        if (file.isEmpty()) {
            throw new IllegalArgumentException("Uploaded file is empty");
        }
        if (file.getSize() > MAX_UPLOAD_SIZE.toBytes()) {
            throw new IllegalArgumentException(String.format(
                    "File size exceeds limit of %s (provided: %d bytes)",
                    MAX_UPLOAD_SIZE, file.getSize()));
        }
    }

    private String resolveCorrelationId(String incoming) {
        return (incoming != null && !incoming.isBlank()) ? incoming : UUID.randomUUID().toString();
    }

    // ---------------------------------------------------------------------------------------------
    // Request / Response DTOs
    // ---------------------------------------------------------------------------------------------

    /**
     * Immutable request body for JSON anonymization.
     */
    public record JsonAnonymizationRequest(
            @NotNull @Parameter(description = "Arbitrary JSON payload to be anonymized")
            Map<String, Object> payload,

            @NotEmpty @Parameter(description = "A map of field names to anonymization rules")
            Map<String, String> rules
    ) { }

    /**
     * Minimal JSON payload wrapper around {@link AnonymizedPayload}.
     */
    public record JsonAnonymizationResponse(
            @Schema(description = "Anonymized version of the original payload")
            Map<String, Object> payload
    ) { }
}